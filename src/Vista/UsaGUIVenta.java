    /** Formatea una venta completa para mostrar en el área de resultados */
    private String formatearVenta(Venta v) {
        StringBuilder sb = new StringBuilder();
        sb.append("==========================================\n");
        sb.append("N Venta           : ").append(v.getNumero()).append("\n");
        sb.append("Estado            : ").append(estadoTexto(v.getEstado())).append("\n");
        sb.append("Fecha generacion  : ").append(v.getFechaHoraGeneracion()).append("\n");
        sb.append("Ultima act.       : ").append(v.getFechaHoraActualizacion()).append("\n");
 
        if (v.getSuCliente() != null) {
            Cliente c = v.getSuCliente();
            sb.append("---- CLIENTE ----\n");
            sb.append("  Nombre     : ").append(c.getNombre()).append("\n");
            sb.append("  Tipo ID    : ").append(c.getTipoIdentificacion() == 'C' ? "Cedula" : "NIT").append("\n");
            sb.append("  Num. ID    : ").append(c.getNumeroIdentificacion()).append("\n");
            sb.append("  Email      : ").append(c.getEmail()).append("\n");
            sb.append("  Telefono   : ").append(c.getTelefono()).append("\n");
            sb.append("  Contacto   : ").append(c.getNombreContacto()).append("\n");
            sb.append("  Empresa    : ").append(c.isEmpresa() ? "Si" : "No").append("\n");
            sb.append("  Descuento  : ").append(c.getPorcentajeDescuento()).append("%\n");
        }
 
        sb.append("---- PAQUETES ----\n");
        sb.append("  Cantidad paquetes    : ").append(v.getSusPaquetesTuristicos().size()).append("\n");
        sb.append("  Total unidades       : ").append(v.calcularCantidadTotalUnidadesPaquetes()).append("\n");
        sb.append("  Valor total paquetes : $").append(v.calcularValorTotalPaquetes()).append("\n");
        sb.append("  Valor descuento      : $").append(v.calcularValorDescuento()).append("\n");
        sb.append("  TOTAL A PAGAR        : $").append(v.calcularValorTotalPagar()).append("\n");
 
        // ✅ Si la venta está cancelada NO se muestra el detalle de paquetes
        if (v.getEstado() == 'C') {
            sb.append("  (venta cancelada — detalle de paquetes no disponible)\n");
        } else {
            int idx = 1;
            for (PaqueteTuristico p : v.getSusPaquetesTuristicos()) {
                boolean esUnico = p instanceof PaqueteTuristicoUnico;
                sb.append("  -- Paquete ").append(idx++).append(" [")
                  .append(esUnico ? "Unico" : "Multiple").append("] --\n");
                sb.append("     Codigo       : ").append(p.getCodigo()).append("\n");
                sb.append("     Nombre       : ").append(p.getNombre()).append("\n");
                sb.append("     Tipologia    : ").append(p.getTipologiaTurismo()).append("\n");
                sb.append("     Origen       : ").append(p.getOrigen()).append("\n");
                sb.append("     Tarifa/dia   : $").append(p.getTarifaDia()).append("\n");
                sb.append("     Duracion     : ").append(p.calcularDuracionTotalDias()).append(" dia(s)\n");
                sb.append("     Hotel        : ").append(p.isHotel()      ? "Incluido"  : "No incluido").append("\n");
                sb.append("     Alimentacion : ").append(p.isAlimentacion()
                    ? (p.isAlimentacionTodo() ? "Todo incluido" : "Solo desayuno")
                    : "No incluida").append("\n");
                sb.append("     Vuelo        : ").append(p.isVuelo()      ? "Incluido"  : "No incluido").append("\n");
                sb.append("     Asistencia   : ").append(p.isAsistencia() ? "Incluida"  : "No incluida").append("\n");
                if (esUnico) {
                    PaqueteTuristicoUnico pu = (PaqueteTuristicoUnico) p;
                    sb.append("     Hotel nombre : ").append(pu.getNombreHotel()).append("\n");
                } else {
                    PaqueteTuristicoMultiple pm = (PaqueteTuristicoMultiple) p;
                    sb.append("     Destino ini. : ").append(pm.obtenerDestinoInicial().getNombreLugar()).append("\n");
                    sb.append("     Destino fin. : ").append(pm.obtenerDestinoFinal().getNombreLugar()).append("\n");
                    sb.append("     Obsequio     : ").append(pm.getObsequio()).append("\n");
                }
                sb.append("     Valor unidad : $").append(p.calcularValorUnidad()).append("\n");
                sb.append("     Valor total  : $").append(p.calcularValorTotal()).append("\n");
            }
        }
        return sb.toString();
    }
 
    private String estadoTexto(char estado) {
        return switch (estado) {
            case 'A' -> "Activa";
            case 'P' -> "Pagada";
            case 'C' -> "Cancelada";
            default  -> String.valueOf(estado);
        };
    }
 
    private String consultarTodasVentas(ArrayList<Venta> ventas) {
        if (ventas.isEmpty()) return "No hay ventas registradas.";
        StringBuilder sb = new StringBuilder("=== TODAS LAS VENTAS ===\n\n");
        for (Venta v : ventas) sb.append(formatearVenta(v));
        sb.append("\nTotal registradas: ").append(ventas.size());
        return sb.toString();
    }
 
    private String consultarVentaDadoNumero(ArrayList<Venta> ventas, int numero) {
        for (Venta v : ventas) {
            if (v.getNumero() == numero)
                return "=== VENTA ENCONTRADA ===\n\n" + formatearVenta(v);
        }
        return "No se encontró ninguna venta con el número " + numero + ".";
    }
 
