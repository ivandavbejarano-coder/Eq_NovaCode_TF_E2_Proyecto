    /**
     * Exporta todas las ventas a un archivo CSV que puede abrirse como Excel.
     * Genera el archivo "ventas_exportadas.csv" en el directorio de trabajo.
     * agregado que se implemento para hacer lo más real posible el projecto
     */
    private void exportarVentasCSV() {
        if (datosVentas.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay ventas para exportar.", "AVISO", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try (java.io.PrintWriter pw = new java.io.PrintWriter(
                new java.io.OutputStreamWriter(
                    new java.io.FileOutputStream("ventas_exportadas.csv"), java.nio.charset.StandardCharsets.UTF_8))) {
            // Encabezados
            pw.println("Num.Venta;Estado;FechaGeneracion;NombreCliente;TipoID;NumeroID;Email;Telefono;" +
                       "Empresa;Descuento%;CodigoPaquete;NombrePaquete;TipoPaquete;Tipologia;Origen;" +
                       "TarifaDia;DiasDuracion;Unidades;ValorUnidad;ValorTotalPaquete;" +
                       "ValorTotalVenta;ValorDescuento;TotalAPagar");
            for (Venta v : datosVentas) {
                Cliente c = v.getSuCliente();
                String nombreCli = c != null ? c.getNombre() : "";
                String tipoId    = c != null ? String.valueOf(c.getTipoIdentificacion()) : "";
                String numId     = c != null ? c.getNumeroIdentificacion() : "";
                String email     = c != null ? c.getEmail() : "";
                String telefono  = c != null ? c.getTelefono() : "";
                String empresa   = c != null ? (c.isEmpresa() ? "Si" : "No") : "";
                double desc      = c != null ? c.getPorcentajeDescuento() : 0;

                for (PaqueteTuristico p : v.getSusPaquetesTuristicos()) {
                    String tipoPaq = (p instanceof PaqueteTuristicoUnico) ? "Unico" : "Multiple";
                    pw.printf("%d;%s;%s;%s;%s;%s;%s;%s;%s;%.1f;%s;%s;%s;%s;%s;%d;%d;%d;%d;%d;%d;%d;%d%n",
                        v.getNumero(),
                        estadoTexto(v.getEstado()),
                        v.getFechaHoraGeneracion(),
                        nombreCli, tipoId, numId, email, telefono, empresa, desc,
                        p.getCodigo(), p.getNombre(), tipoPaq, p.getTipologiaTurismo(), p.getOrigen(),
                        p.getTarifaDia(), p.calcularDuracionTotalDias(), p.getCantidadUnidades(),
                        p.calcularValorUnidad(), p.calcularValorTotal(),
                        v.calcularValorTotalPaquetes(), v.calcularValorDescuento(), v.calcularValorTotalPagar());
                }
            }
            JOptionPane.showMessageDialog(null,
                "Exportación exitosa.\nArchivo: ventas_exportadas.csv\n" +
                "Ábralo con Excel o LibreOffice Calc.", "EXPORTADO", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al exportar:\n" + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

