    /** Normaliza texto: quita tildes y pasa a mayúsculas para comparar ciudades */
    private String normalizarCiudad(String ciudad) {
        String s = ciudad.trim().toUpperCase();
        s = s.replace("\u00C1","A").replace("\u00C9","E").replace("\u00CD","I")
             .replace("\u00D3","O").replace("\u00DA","U").replace("\u00DC","U")
             .replace("\u00D1","N")
             .replace("\u00E1","A").replace("\u00E9","E").replace("\u00ED","I")
             .replace("\u00F3","O").replace("\u00FA","U").replace("\u00FC","U")
             .replace("\u00F1","N");
        return s;
    }

    private boolean esCiudadValida(String ciudad) {
        return CIUDADES_COLOMBIA.contains(normalizarCiudad(ciudad));
    }

