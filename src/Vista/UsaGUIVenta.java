    private void jButtonPorCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPorCategoriaActionPerformed
        // Consultar ventas por categoría de paquete (Único o Múltiple)
        String categoriaSeleccionada = (String) jComboBoxCategoriaBusq.getSelectedItem();
        StringBuilder sb = new StringBuilder();
        sb.append("=== VENTAS CON PAQUETES ").append(categoriaSeleccionada.toUpperCase()).append(" ===\n\n");
        boolean hayResultados = false;
        for (Venta v : datosVentas) {
            for (PaqueteTuristico p : v.getSusPaquetesTuristicos()) {
                boolean esUnico    = p instanceof Modelo.PaqueteTuristicoUnico;
                boolean esMultiple = p instanceof Modelo.PaqueteTuristicoMultiple;
                if ((categoriaSeleccionada.equals("Único") && esUnico)
                 || (categoriaSeleccionada.equals("Múltiple") && esMultiple)) {
                    sb.append(formatearVenta(v));
                    hayResultados = true;
                    break;
                }
            }
        }
        if (!hayResultados) {
            sb.append("No se encontraron ventas con paquetes de categoría ")
              .append(categoriaSeleccionada).append(".");
        }
        jTextAreaResultados.setText(sb.toString());
    }//GEN-LAST:event_jButtonPorCategoriaActionPerformed

