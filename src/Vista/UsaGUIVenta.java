    private void jButtonPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPagarActionPerformed
        // Cambiar estado de venta a PAGADA
        try {
            if (jTextFieldNumActualizar.getText().isBlank()) {
                JOptionPane.showMessageDialog(null,
                    "Ingrese el número de venta a pagar.",
                    "CAMPO VACÍO", JOptionPane.WARNING_MESSAGE);
                return;
            }
            int numVenta = Integer.parseInt(jTextFieldNumActualizar.getText());
            boolean encontrada = false;
            for (Venta v : datosVentas) {
                if (v.getNumero() == numVenta) {
                    if (v.getEstado() == 'C') {
                        JOptionPane.showMessageDialog(null,
                            "No se puede pagar una venta cancelada.",
                            "ERROR", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                    v.setEstado('P');
                    v.setFechaHoraActualizacion(LocalDateTime.now());
                    encontrada = true;
                    JOptionPane.showMessageDialog(null,
                        "Venta #" + numVenta + " marcada como PAGADA.",
                        "ÉXITO", JOptionPane.INFORMATION_MESSAGE);
                    break;
                }
            }
            if (!encontrada) {
                JOptionPane.showMessageDialog(null,
                    "No existe una venta con el número " + numVenta,
                    "NO ENCONTRADA", JOptionPane.WARNING_MESSAGE);
            }
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(null,
                "El número de venta debe ser un valor numérico.",
                "ERROR DE FORMATO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonPagarActionPerformed

