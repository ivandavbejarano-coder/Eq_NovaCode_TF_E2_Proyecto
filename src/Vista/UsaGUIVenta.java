    private void jButtonGuardarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarVentaActionPerformed
        // Construir y registrar la venta a partir de los campos del formulario
                try {
            // 1. Campos obligatorios
            if (jTextFieldNombreCliente2.getText().isBlank()
             || jTextFieldNumeroID2.getText().isBlank()
             || jTextFieldCodigoPaquete2.getText().isBlank()
             || jTextFieldNombrePaquete2.getText().isBlank()
             || jTextFieldTarifaDia2.getText().isBlank()
             || jTextFieldDias3.getText().isBlank()
             || jTextFieldTipologia.getText().isBlank()) {
                JOptionPane.showMessageDialog(null,
                    "Complete todos los campos obligatorios:\n"
                    + "Nombre Cliente, Núm. ID, Código Paquete,\n"
                    + "Nombre Paquete, Tipología, Tarifa/Día y Días.",
                    "CAMPOS VACÍOS", JOptionPane.WARNING_MESSAGE);
                return;

            }
 
            // 2. Nombre del cliente — solo letras, espacios y tildes
            String nombre = jTextFieldNombreCliente2.getText().trim();
            if (!nombre.matches("[a-zA-ZáéíóúÁÉÍÓÚüÜñÑ' -]{3,}")) {
                JOptionPane.showMessageDialog(null,
                    "El nombre del cliente solo puede contener letras y espacios\n"
                    + "y debe tener al menos 3 caracteres.",
                    "NOMBRE INVÁLIDO", JOptionPane.WARNING_MESSAGE);
                jTextFieldNombreCliente2.requestFocus();
                return;
            }
 
            // 3. Número de ID según tipo
            String numId = jTextFieldNumeroID2.getText().trim();
            char tipoIdValidar = jComboBoxTipoID2.getSelectedItem().toString().charAt(0);
            if (tipoIdValidar == 'N' && !numId.matches("\\d{9}")) {
                JOptionPane.showMessageDialog(null,
                    "El NIT debe tener exactamente 9 dígitos.",
                    "ID INVÁLIDO", JOptionPane.WARNING_MESSAGE);
                jTextFieldNumeroID2.requestFocus();
                return;
            } else if (tipoIdValidar == 'C' && !numId.matches("\\d{6,}")) {
                JOptionPane.showMessageDialog(null,
                    "La cédula debe tener mínimo 6 dígitos.",
                    "ID INVÁLIDO", JOptionPane.WARNING_MESSAGE);
                jTextFieldNumeroID2.requestFocus();
                return;
            }
 
            // 4. Email (opcional pero válido si se ingresa)
            String email = jTextFieldEmail2.getText().trim();
            if (!email.isEmpty() && !email.matches(
                    "^[a-zA-Z0-9._%+\\-]+@[a-zA-Z0-9\\-]+(\\.[a-zA-Z0-9\\-]+)*\\.[a-zA-Z]{2,}$")) {
                JOptionPane.showMessageDialog(null,
                    "El email no tiene un formato válido.\nEjemplo: usuario@correo.com",
                    "EMAIL INVÁLIDO", JOptionPane.WARNING_MESSAGE);
                jTextFieldEmail2.requestFocus();
                return;
            }
 
            // 5. Teléfono — exactamente 10 dígitos
            String telefono = jTextFieldTelefono2.getText().trim();
            if (!telefono.matches("\\d{10}")) {
                JOptionPane.showMessageDialog(null,
                    "El teléfono debe contener exactamente 10 dígitos.\nEjemplo: 3001234567",
                    "TELÉFONO INVÁLIDO", JOptionPane.WARNING_MESSAGE);
                jTextFieldTelefono2.requestFocus();
                return;
            }
 
            // 6. Descuento — entre 0 y 70
            double descuento = 0.0;
            if (!jTextFieldDescuento2.getText().isBlank()) {
                try {
                    descuento = Double.parseDouble(jTextFieldDescuento2.getText().trim());
                    if (descuento < 0 || descuento > 70) {
                        JOptionPane.showMessageDialog(null,
                            "El descuento debe ser un valor entre 0 y 70.",
                            "DESCUENTO INVÁLIDO", JOptionPane.WARNING_MESSAGE);
                        jTextFieldDescuento2.requestFocus();
                        return;
                    }
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null,
                        "El descuento debe ser un valor numérico (ej: 10 o 10.5).",
                        "DESCUENTO INVÁLIDO", JOptionPane.WARNING_MESSAGE);
                    jTextFieldDescuento2.requestFocus();
                    return;
                }
            }
 
            // 7. Tipología
            
            String tipologia = jTextFieldTipologia.getText().trim();
            if (!tipologia.matches("[a-zA-ZáéíóúÁÉÍÓÚüÜñÑ,. ]{3,}")) {
                JOptionPane.showMessageDialog(null,
                    "La tipología solo puede contener letras y espacios.\\n"
                    + "Ej: negocios, recreación, educativo, ecológico",
                    "TIPOLOGÍA INVÁLIDA",JOptionPane.WARNING_MESSAGE);
                jTextFieldTipologia.requestFocus(); return;
            }

            // 8. Descripción — máximo 500 caracteres, no solo números
            String descripcion = jTextAreaDescripcion.getText().trim();
            if (descripcion.length() > 500) {
                JOptionPane.showMessageDialog(null,"La descripción no puede superar 500 caracteres.\nActualmente: "+descripcion.length()+" caracteres.","DESCRIPCIÓN INVÁLIDA",JOptionPane.WARNING_MESSAGE);
                jTextAreaDescripcion.requestFocus(); return;
            }
            if (!descripcion.isEmpty() && descripcion.matches("\\d+")) {
                JOptionPane.showMessageDialog(null,"La descripción no puede contener solo números.\nDescribe el paquete con texto.","DESCRIPCIÓN INVÁLIDA",JOptionPane.WARNING_MESSAGE);
                jTextAreaDescripcion.requestFocus(); return;
            }

            // 9. Nombre paquete — mínimo 10 caracteres
            String nombrePaq = jTextFieldNombrePaquete2.getText().trim();
            if (nombrePaq.length() < 10) {
                JOptionPane.showMessageDialog(null,"El nombre del paquete debe tener mínimo 10 caracteres.","NOMBRE INVÁLIDO",JOptionPane.WARNING_MESSAGE);
                jTextFieldNombrePaquete2.requestFocus(); return;
            }

            // 10. Origen — ciudad colombiana válida
            String origen = jTextFieldOrigen2.getText().trim();
            if (!origen.isEmpty() && !esCiudadValida(origen)) {
                JOptionPane.showMessageDialog(null,
                    "El origen debe ser una ciudad colombiana válida"
                    + "Ej: Cali, Medellín, Bogotá  D.C., Cartagena, Pasto...",
                    "CIUDAD INVALIDA",JOptionPane.WARNING_MESSAGE);
                jTextFieldOrigen2.requestFocus(); return;
            }

            // 11. Destino — ciudad colombiana válida
            String nombreDest = jTextFieldDestino3.getText().trim();
            if (!nombreDest.isEmpty() && !esCiudadValida(nombreDest)) {
                JOptionPane.showMessageDialog(null,
                    "El destino debe ser una ciudad colombiana válida"
                    + "Ej: Cali, Medellín, Bogotá D.C., Cartagena, Pasto...",
                    "CIUDAD INVÁLIDA",JOptionPane.WARNING_MESSAGE);
                jTextFieldDestino3.requestFocus(); return;
            }

            // 12. Tarifa/día — entero positivo
            int tarifaDia;
            try {
                tarifaDia = Integer.parseInt(jTextFieldTarifaDia2.getText().trim());
                if (tarifaDia <= 0) throw new NumberFormatException();
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null,"La tarifa por deuda debe ser un nÚmero entero positivo.","TARIFA INVÁLIDA",JOptionPane.WARNING_MESSAGE);
                jTextFieldTarifaDia2.requestFocus(); return;
            }

            // 13. Días — entre 1 y 365
            int diasDest;
            try {
                diasDest = Integer.parseInt(jTextFieldDias3.getText().trim());
                if (diasDest < 1 || diasDest > 365) throw new NumberFormatException();
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null,"Los días deben ser un numero entre 1 y 365.","DÍAS INVÁLIDOS",JOptionPane.WARNING_MESSAGE);
                jTextFieldDias3.requestFocus(); return;
            }

            // 14. Unidades — entre 1 y 500
            int unidades = 1;
            if (!jTextFieldUnidades2.getText().isBlank()) {
                try {
                    unidades = Integer.parseInt(jTextFieldUnidades2.getText().trim());
                    if (unidades < 1 || unidades > 500) throw new NumberFormatException();
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null,"Las unidades deben ser un número entre 1 y 500.","UNIDADES INVÁLIDAs",JOptionPane.WARNING_MESSAGE);
                    jTextFieldUnidades2.requestFocus(); return;
                }
            }

            // 15. Validación alimentación/desayuno
            boolean alimentacion = jCheckBoxAlimentacion.isSelected();
            if (jCheckBoxSoloDesayuno.isSelected() && !alimentacion) {
                JOptionPane.showMessageDialog(null,"No puede seleccionar 'Solo Desayuno' sin marcar 'Alimentación'.","ALIMENTACIÓN INVÁLIDA",JOptionPane.WARNING_MESSAGE);
                jCheckBoxAlimentacion.requestFocus(); return;
            }
            boolean alimentacionTodo = alimentacion && !jCheckBoxSoloDesayuno.isSelected();
            boolean hotel      = jCheckBoxHotel.isSelected();
            boolean vuelo      = jCheckBoxVuelo.isSelected();
            boolean asistencia = jCheckBoxAsistencia.isSelected();

            // Construir cliente
            char    tipoId         = jComboBoxTipoID2.getSelectedItem().toString().charAt(0);
            boolean empresa        = jComboBoxEmpresa2.getSelectedItem().toString().equals("True");
            String  nombreContacto = jTextFieldNombreContacto2.getText().trim();
            String  codigoPaq      = jTextFieldCodigoPaquete2.getText().trim();
            Cliente cliente = new Cliente(tipoId, numId, empresa, nombre, email, telefono, nombreContacto, descuento);

            // Construir destino y paquete
            LinkedList<String> atractivos = new LinkedList<>();
            ArrayList<Destino> destinos   = new ArrayList<>();
            destinos.add(new Destino(nombreDest, diasDest, atractivos, false));

            String categoria = jComboBoxCategoria2.getSelectedItem().toString();
            PaqueteTuristico paquete;
            if (categoria.equals("Único")) {
                paquete = new PaqueteTuristicoUnico("Hotel General", codigoPaq, nombrePaq,
                    tipologia, descripcion, origen, destinos, alimentacionTodo, tarifaDia, unidades);
            } else {
                paquete = new PaqueteTuristicoMultiple("Sin obsequio", codigoPaq, nombrePaq,
                    tipologia, descripcion, origen, destinos, alimentacionTodo, tarifaDia, unidades);
            }
            paquete.setHotel(hotel);
            paquete.setAlimentacion(alimentacion);
            paquete.setVuelo(vuelo);
            paquete.setAsistencia(asistencia);

            ArrayList<PaqueteTuristico> paquetes = new ArrayList<>();
            paquetes.add(paquete);
            int           numVenta = generarNumeroVenta(datosVentas);
            LocalDateTime ahora    = LocalDateTime.now();
            Venta venta = new Venta(numVenta, ahora, ahora, cliente, paquetes, 'A');
            datosVentas.add(venta);

            JOptionPane.showMessageDialog(null,
                "Venta #"+numVenta+" registrada exitosamente.\nValor total a pagar: $"+venta.calcularValorTotalPagar(),
                "VENTA GUARDADA",JOptionPane.INFORMATION_MESSAGE);
            jButtonLimpiarActionPerformed(null);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error al registrar la venta:\n"+e.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonGuardarVentaActionPerformed

