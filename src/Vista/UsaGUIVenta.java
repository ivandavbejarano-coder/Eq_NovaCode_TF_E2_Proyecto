    /** Formatea una Venta en texto legible para el area de resultados */
    public int generarNumeroVenta(ArrayList<Venta> ventas) {
        int max = 0;
        for (Venta v : ventas) {
            if (v.getNumero() > max) max = v.getNumero();
        }
        return max + 1;
    }
